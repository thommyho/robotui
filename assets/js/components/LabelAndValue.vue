.root {
	margin-bottom: 1rem;
}
.label {
	text-transform: uppercase;
	color: var(--robotui-gray);
	font-size: 14px;
}
.value {
	font-size: 18px;
}
.extraValue {
	color: var(--robotui-gray);
	font-size: 14px;
}
</style>
